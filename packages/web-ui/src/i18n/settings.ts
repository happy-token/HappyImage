export function t(lang: string, key: string, vars?: Record<string, string>): string {
  const map = lang === 'en' ? en : zh
  let text = map[key] || zh[key] || key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, v)
    }
  }
  return text
}
