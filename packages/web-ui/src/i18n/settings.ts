import { useEffect, useState } from 'react'

export type AppLanguage = 'zh' | 'en'

export const LANGUAGE_EVENT = 'happyimage-language-change'

export function normalizeLanguage(value?: string | null): AppLanguage {
  return String(value || '').slice(0, 2) === 'en' ? 'en' : 'zh'
}

function readStoredLanguage(): AppLanguage {
  if (typeof window === 'undefined') return 'zh'
  return normalizeLanguage(window.localStorage.getItem('happyimage-language') || document.documentElement.lang)
}

export function setAppLanguage(value?: string | null) {
  const lang = normalizeLanguage(value)
  if (typeof document !== 'undefined') document.documentElement.lang = lang === 'en' ? 'en' : 'zh-CN'
  if (typeof window !== 'undefined') {
    window.localStorage.setItem('happyimage-language', lang)
    window.dispatchEvent(new CustomEvent(LANGUAGE_EVENT, { detail: lang }))
  }
}

export function useAppLanguage(): AppLanguage {
  const [lang, setLang] = useState<AppLanguage>(() => readStoredLanguage())

  useEffect(() => {
    let cancelled = false
    fetch('/api/settings')
      .then(res => res.json())
      .then(data => {
        if (cancelled) return
        const next = normalizeLanguage(data?.DEFAULT_LANGUAGE)
        setLang(next)
        setAppLanguage(next)
      })
      .catch(() => undefined)

    const onLanguage = (event: Event) => {
      const detail = (event as CustomEvent<string>).detail
      setLang(detail ? normalizeLanguage(detail) : readStoredLanguage())
    }
    window.addEventListener(LANGUAGE_EVENT, onLanguage)
    window.addEventListener('storage', onLanguage)
    return () => {
      cancelled = true
      window.removeEventListener(LANGUAGE_EVENT, onLanguage)
      window.removeEventListener('storage', onLanguage)
    }
  }, [])

  return lang
}

